def dell(a: int, b: int):
    if b == 0:
        return "0"
    else:
        return a / b

def mult(a: int, b: int):
        return a * b

def sum(a: int, b: int):
        return a + b

def minus(a: int, b: int):
        return a - b

def degree(a: int, b: int):
        return a ** b